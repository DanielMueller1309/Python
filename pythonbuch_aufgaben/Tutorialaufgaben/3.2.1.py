# Programm um mithilfe einer temp variable werte zweier Variablen zu vertauschen
erste_zahl = 7
print("erste_zahl: ", erste_zahl)
zweite_zahl = 9
print("zweite_zahl: ", zweite_zahl)
temp = erste_zahl
print("temp: ", temp)
erste_zahl = zweite_zahl
print("erste_zahl: ", erste_zahl)
zweite_zahl = temp
print("zweite_zahl: ", zweite_zahl)
